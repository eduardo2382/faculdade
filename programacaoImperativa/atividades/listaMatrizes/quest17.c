#include <stdio.h>

int main(){
    int matriz[10][10] = {
        {15, 3, 18, 7, 12, 0, 19, 5, 14, 8},
        {2, 17, 9, 4, 11, 6, 20, 1, 13, 10},
        {16, 8, 5, 19, 2, 14, 7, 11, 3, 18},
        {9, 12, 0, 6, 17, 4, 13, 10, 15, 1},
        {5, 20, 11, 8, 3, 19, 2, 16, 7, 14},
        {18, 4, 15, 12, 9, 1, 6, 13, 0, 17},
        {11, 7, 20, 3, 10, 5, 14, 2, 19, 8},
        {6, 13, 1, 15, 18, 9, 4, 11, 12, 0},
        {17, 2, 14, 7, 16, 3, 20, 10, 5, 19},
        {8, 9, 6, 11, 4, 13, 15, 1, 18, 12}
    };

    float quant, soma, media = 0;

    for(int l = 0; l < 10; l++){
        for(int c = 0; c < 10; c++){
            if(l == c){
                quant++;
                soma += matriz[l][c];
            }
        }
    }

    media = soma/quant;

    printf("%.2f", media);
}